package org.picketlink.identity.federation.saml.v2.assertion;

import java.io.Serializable;
import java.net.URI;

/**
 * Type that represents an AuthnContextDeclRef
 * @author Anil.Saldhana@redhat.com
 * @since Nov 24, 2010
 */
public class AuthnContextDeclRefType implements URIType, Serializable
{
   private static final long serialVersionUID = 1L;
   private URI value;

   public AuthnContextDeclRefType(URI value)
   { 
      this.value = value;
   }

   public URI getValue()
   {
      return value;
   }

   @Override
   public int hashCode()
   {
      final int prime = 31;
      int result = 1;
      result = prime * result + ((value == null) ? 0 : value.hashCode());
      return result;
   }

   @Override
   public boolean equals(Object obj)
   {
      if (this == obj)
         return true;
      if (obj == null)
         return false;
      if (getClass() != obj.getClass())
         return false;
      AuthnContextDeclRefType other = (AuthnContextDeclRefType) obj;
      if (value == null)
      {
         if (other.value != null)
            return false;
      }
      else if (!value.equals(other.value))
         return false;
      return true;
   }  
}
